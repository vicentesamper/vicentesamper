
public class Jueves20_FOR {
	public static void main(String[] args) {
		int contador;
		// for (inicio,condición,acción) (lo que pasa si se cumple la condición)
		for (contador = 0; contador < 10; contador=contador+2) {  // Empieza por 0, como se cumple la condición 
			System.out.print("HOLA ");
		}
	}
} // Nos devuelve 5 veces Hola por las posiciones 0, 2, 4, 6, 8
